export class Ancestors {
  isOpen = false;

  private root = document.getElementById("ancestors")!;
  private textEl = document.getElementById("ancestorsText")!;

  constructor() {
    document.getElementById("ancestorsClose")!.onclick = () => this.close();
    this.textEl.innerHTML = LORE.map((p) => `<p>${p}</p>`).join("");
  }

  open(): void {
    this.isOpen = true;
    this.root.classList.remove("hidden");
    this.textEl.scrollTop = 0;
    window.addEventListener("keydown", this.onKey, true);
  }

  close(): void {
    this.isOpen = false;
    this.root.classList.add("hidden");
    window.removeEventListener("keydown", this.onKey, true);
  }

  private onKey = (e: KeyboardEvent): void => {
    if (!this.isOpen) return;
    if (e.key === "Escape") {
      e.preventDefault();
      e.stopPropagation();
      this.close();
    }
  };
}
